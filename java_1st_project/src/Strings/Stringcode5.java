package Strings;

public class Stringcode5 {
    public static void main(String[] args) {
        String s1 = "Rama";
        String s2 = "Rama";

        if (s1.compareTo(s2) == 0) {
            System.out.println(s1.compareTo(s2));
            System.out.println("String are equals");
        } else {
            System.out.println(s1.compareTo(s2));
            System.out.println("String are not equals");
        }

        // 0
        // String are equals
    }
}
